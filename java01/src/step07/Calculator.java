/* 더하기, 빼기, 곱하기, 나누기 메서드를 모아둔다.

*/
package step07;
public class Calculator{
  static int plus(int a, int b) {
    return a + b;
  }
  static int minus(int a, int b) {
    return a - b;
  }
  static int multiple(int a, int b) {
    return a * b;
  }
  static int divide(int a, int b) {
    return a / b;
  }
}
