/* 주제: 비교 연산자 사용 */
package step04;

public class Exam02 {
  public static void main(String[] args) {
    System.out.printf("10 == 11 ? %b\n", 10 == 11);  // f
    System.out.printf("10 != 11 ? %b\n", 10 != 11);  // t
    System.out.printf("10 < 11 ? %b\n", 10 < 11);  // t
    System.out.printf("10 <= 11 ? %b\n", 10 <= 11);  // t
    System.out.printf("10 > 11 ? %b\n", 10 > 11);  // f
    System.out.printf("10 >= 11 ? %b\n", 10 >= 11);  // f

  }
}
